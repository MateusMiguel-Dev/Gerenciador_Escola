    private void btSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSubmitActionPerformed
        //BOTÃO OK [SUBMIT]
        String nome = tfNome.getText();
        String senha_usuario = tfSenha.getText();
        String senha = geraSenha(nome);
        boolean verifique = verificaSenha(senha, senha_usuario);
        
        if(verifique){
            this.dispose();
            FormSistema sistema = new FormSistema();
            sistema.setVisible(true);
            sistema.setLocationRelativeTo(null);
        }else{
            String op = JOptionPane.showInputDialog("Senha Incorreta! Digite 1 para tentar novamente ou 0 para sair:");
            if(op.equals("0")){
                System.exit(0);
            }else{
                tfNome.setText(null);
                tfSenha.setText(null);
            }
        }
    }//GEN-LAST:event_btSubmitActionPerformed

