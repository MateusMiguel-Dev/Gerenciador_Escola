    public static String geraSenha(String nome){// -- FUNÇÃO 1
        String[] aux = nome.split(" ");
        String p_nome = aux[0].toUpperCase();
        LocalDate data_atual = LocalDate.now();
        int dia = data_atual.getDayOfMonth();
        int mes = data_atual.getMonthValue();
        int ano = data_atual.getYear();
        String data = Integer.toString(dia).concat(Integer.toString(mes).concat(Integer.toString(ano)));
        return p_nome.concat(data);
    }
    public static boolean verificaSenha(String senha, String senha_usuario){// -- FUNÇÃO 2 
        return senha.equals(senha_usuario);
    }
    
